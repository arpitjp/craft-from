"""Stage 4: Block Grid → Litematica / Schematic / Sponge Schematic file.

Supports:
- .litematic (Litematica mod) via litemapy
- .schematic (MCEdit / WorldEdit classic) via nbtlib
- .schem (Sponge / modern WorldEdit) via nbtlib
"""

from pathlib import Path

import nbtlib

from api.core.types import BlockGrid, PipelineConfig, SchematicOutput
from api.utils.logging import get_logger

log = get_logger(__name__)

SUPPORTED_FORMATS = ("litematic", "schematic", "schem")


def run(block_grid: BlockGrid, config: PipelineConfig) -> SchematicOutput:
    fmt = config.output_format
    if fmt == "litematic":
        return _export_litematic(block_grid, config)
    elif fmt == "schematic":
        return _export_schematic(block_grid, config)
    elif fmt == "schem":
        return _export_sponge(block_grid, config)
    else:
        raise ValueError(
            f"Unsupported format: {fmt}. Supported: {SUPPORTED_FORMATS}"
        )


def export_format(
    block_grid: BlockGrid,
    output_dir: Path,
    fmt: str,
    colorless: bool = False,
) -> Path:
    """Export a block grid to a specific format on disk. Returns the file path.

    If colorless=True, every block is replaced with minecraft:white_concrete.
    """
    grid = block_grid
    if colorless:
        grid = _make_colorless(block_grid)

    output_dir.mkdir(parents=True, exist_ok=True)

    if fmt == "litematic":
        return _write_litematic(grid, output_dir / "output.litematic")
    elif fmt == "schematic":
        return _write_schematic(grid, output_dir / "output.schematic")
    elif fmt == "schem":
        return _write_sponge(grid, output_dir / "output.schem")
    else:
        raise ValueError(f"Unsupported format: {fmt}. Supported: {SUPPORTED_FORMATS}")


def _make_colorless(block_grid: BlockGrid) -> BlockGrid:
    """Return a copy of the grid with all blocks set to white_concrete."""
    from api.core.types import BlockMapping
    blocks = [
        BlockMapping(
            block_id="minecraft:white_concrete",
            position=b.position,
            reason="colorless",
        )
        for b in block_grid.blocks
    ]
    return BlockGrid(
        blocks=blocks,
        dimensions=block_grid.dimensions,
        palette=["minecraft:white_concrete"],
    )


# ── Litematica (.litematic) ─────────────────────────────────────────────

def _write_litematic(block_grid: BlockGrid, output_path: Path) -> Path:
    try:
        from litemapy import Region, BlockState
    except ImportError:
        raise ImportError("Install litemapy: pip install litemapy")

    dx, dy, dz = block_grid.dimensions
    reg = Region(0, 0, 0, dx, dy, dz)

    unique_states: dict[str, BlockState] = {}
    placed = 0

    for block in block_grid.blocks:
        bid = block.block_id
        if bid not in unique_states:
            unique_states[bid] = BlockState(bid)
        x, y, z = block.position
        if 0 <= x < dx and 0 <= y < dy and 0 <= z < dz:
            reg.setblock(x, y, z, unique_states[bid])
            placed += 1

    schem = reg.as_schematic(
        name="craftfrom_output",
        author="craftfrom",
        description="Generated by CraftFrom",
    )
    schem.save(str(output_path))
    log.info(f"Exported {placed} blocks (litematic) to {output_path}")
    return output_path


def _export_litematic(block_grid: BlockGrid, config: PipelineConfig) -> SchematicOutput:
    config.output_dir.mkdir(parents=True, exist_ok=True)
    path = _write_litematic(block_grid, config.output_dir / "output.litematic")
    return SchematicOutput(
        path=path,
        format="litematic",
        block_count=len(block_grid.blocks),
        dimensions=block_grid.dimensions,
    )


