def _legacy_id(block_id: str) -> tuple[int, int]:
    """Map a modern block ID to legacy (numeric ID, data value).
    Falls back to stone (1, 0) for unmapped blocks.
    """
    return _BLOCK_NAME_TO_LEGACY.get(block_id, (1, 0))


def _write_schematic(block_grid: BlockGrid, output_path: Path) -> Path:
    dx, dy, dz = block_grid.dimensions

    blocks_array = bytearray(dx * dy * dz)
    data_array = bytearray(dx * dy * dz)

    placed = 0
    for block in block_grid.blocks:
        x, y, z = block.position
        if 0 <= x < dx and 0 <= y < dy and 0 <= z < dz:
            idx = (y * dz + z) * dx + x
            bid, dval = _legacy_id(block.block_id)
            blocks_array[idx] = bid & 0xFF
            data_array[idx] = dval & 0xF
            placed += 1

    nbt = nbtlib.Compound({
        "Schematic": nbtlib.Compound({
            "Width": nbtlib.Short(dx),
            "Height": nbtlib.Short(dy),
            "Length": nbtlib.Short(dz),
            "Materials": nbtlib.String("Alpha"),
            "Blocks": nbtlib.ByteArray(blocks_array),
            "Data": nbtlib.ByteArray(data_array),
            "Entities": nbtlib.List[nbtlib.Compound](),
            "TileEntities": nbtlib.List[nbtlib.Compound](),
        })
    })

    nbt_file = nbtlib.File(nbt["Schematic"], gzipped=True)
    nbt_file.save(str(output_path))
    log.info(f"Exported {placed} blocks (schematic) to {output_path}")
    return output_path


def _export_schematic(block_grid: BlockGrid, config: PipelineConfig) -> SchematicOutput:
    config.output_dir.mkdir(parents=True, exist_ok=True)
    path = _write_schematic(block_grid, config.output_dir / "output.schematic")
    return SchematicOutput(
        path=path,
        format="schematic",
        block_count=len(block_grid.blocks),
        dimensions=block_grid.dimensions,
    )


# ── Sponge Schematic v2 (.schem) ────────────────────────────────────────

def _write_sponge(block_grid: BlockGrid, output_path: Path) -> Path:
    dx, dy, dz = block_grid.dimensions

    palette_map: dict[str, int] = {"minecraft:air": 0}
    palette_idx = 1
    block_data_list: list[int] = []

    block_by_pos: dict[tuple[int, int, int], str] = {}
    for block in block_grid.blocks:
        x, y, z = block.position
        if 0 <= x < dx and 0 <= y < dy and 0 <= z < dz:
            block_by_pos[(x, y, z)] = block.block_id

    placed = 0
    for y in range(dy):
        for z in range(dz):
            for x in range(dx):
                bid = block_by_pos.get((x, y, z), "minecraft:air")
                if bid not in palette_map:
                    palette_map[bid] = palette_idx
                    palette_idx += 1
                varint = palette_map[bid]
                while varint > 0x7F:
                    block_data_list.append((varint & 0x7F) | 0x80)
                    varint >>= 7
                block_data_list.append(varint & 0x7F)
                if bid != "minecraft:air":
                    placed += 1

    palette_nbt = nbtlib.Compound({
        nbtlib.String(k): nbtlib.Int(v) for k, v in palette_map.items()
    })

    schem = nbtlib.Compound({
        "Schematic": nbtlib.Compound({
            "Version": nbtlib.Int(2),
            "DataVersion": nbtlib.Int(3463),
            "Width": nbtlib.Short(dx),
            "Height": nbtlib.Short(dy),
            "Length": nbtlib.Short(dz),
            "Palette": palette_nbt,
            "PaletteMax": nbtlib.Int(palette_idx),
            "BlockData": nbtlib.ByteArray(block_data_list),
            "Metadata": nbtlib.Compound({
                "WEOffsetX": nbtlib.Int(0),
                "WEOffsetY": nbtlib.Int(0),
                "WEOffsetZ": nbtlib.Int(0),
            }),
        })
    })

    nbt_file = nbtlib.File(schem["Schematic"], gzipped=True)
    nbt_file.save(str(output_path))
    log.info(f"Exported {placed} blocks (sponge schem) to {output_path}")
    return output_path


def _export_sponge(block_grid: BlockGrid, config: PipelineConfig) -> SchematicOutput:
    config.output_dir.mkdir(parents=True, exist_ok=True)
    path = _write_sponge(block_grid, config.output_dir / "output.schem")
    return SchematicOutput(
        path=path,
        format="schem",
        block_count=len(block_grid.blocks),
        dimensions=block_grid.dimensions,
    )
